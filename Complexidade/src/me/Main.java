package me;

public class Main {

	public static void main(String[] args) {

		int[][] m = { { 4, 10, 21, 34, 39, 40, 57, 62 }, { 9, 17, 28, 35, 41, 42, 58, 72 },//---------------------------- 1
				{ 19, 20, 30, 47, 49, 49, 65, 73 }, { 27, 39, 40, 48, 52, 60, 66, 79 },//---------------------------- 1
				{ 33, 43, 44, 58, 59, 61, 77, 81 }, { 46, 46, 60, 63, 69, 71, 79, 88 },//---------------------------- 1
				{ 56, 61, 62, 68, 72, 76, 87, 88 }, { 64, 67, 69, 73, 83, 89, 90, 91 } };//---------------------------- 1

		int x = 58;//---------------------------- 1
		
		System.out.println(contemOn2(m, x));//---------------------------- 1
		System.out.println(contemOlog(m, x));//---------------------------- 1
	}

	// Complexidade O(n^2)
	
	public static boolean contemOn2(int[][] m, int x) {
		for (int i = 0; i < m.length; i++) { //------------------------ 2 + 2n
			for (int j = 0; j < m.length; j++) {//------------------------ 2 + 2n
				if (m[i][j] == x) { //----------------------------------------n  
					return true;//---------------------------- 1
				} 
			} 
		}
		return false;//---------------------------- 1
	}
	
	// Complexidade O(log n)
	
	public static boolean contemOlog(int[][] m, int x) {
		int i = 0; //---------------------------- 1
		int j = m.length - 1;//----------------------------------------n  
		while (i <= m.length - 1 && j >= 0) {//------------------------ 2 + 2n
			if (m[i][j] == x) {//----------------------------------------n  
				return true; //---------------------------- 1
			} else if (m[i][j] < x) {//----------------------------------------n  
				i++;//----------------------------------------n  
			} else {
				j--;//----------------------------------------n  
			}
		}
		return false;//---------------------------- 1
	}
}

